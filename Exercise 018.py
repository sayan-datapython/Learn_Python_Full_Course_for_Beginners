import math
an = float(input("Enter the angle: "))
seno = math.sin(math.radians(an))
print("The angle is {} and sen is {:.2f}".format(an, seno))
cos = math.cos(math.radians(an))
print("The angle is {} and sen is {:.2f}".format(an, cos))
tang = math.tan(math.radians(an))
print("The angle is {} and sen is {:.2f}".format(an, tang))